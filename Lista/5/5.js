let num = parseInt(prompt("Digite um número inteiro:"));

let raizQuadrada = Math.sqrt(num);
let raizCubica = Math.cbrt(num);
let potencia = Math.pow(num, 2);

alert(`Raiz quadrada: ${raizQuadrada}\nRaiz cúbica: ${raizCubica}\nPotência: ${potencia}`);

/*Math.pow esse codigo faz base^expoente.

Math.sqrt esse codigo faz a raiz quadrada do número

Math.cbrt esse codigo faz a raiz cúbica do número.*/